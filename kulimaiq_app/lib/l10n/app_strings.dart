extension AppStringsDisease on AppStrings {
  /// Returns the localised disease name, or a Title Case fallback for
  /// labels the app doesn't yet know (e.g. newly-trained backend classes).
  String diseaseLabel(String diseaseId) {
    final key = 'disease_$diseaseId';
    final translated = t(key);
    // t() returns the key itself when not found — treat that as "no translation"
    if (translated != key) return translated;
    return _toTitleCase(diseaseId);
  }

  String cropLabel(String cropId) {
    final key = 'crop_$cropId';
    final translated = t(key);
    if (translated != key) return translated;
    return _toTitleCase(cropId);
  }

  String recommendation(String key) => t(key);
  String advisoryTitle(String key) => t(key);
  String advisoryBody(String key) => t(key);
  String severityLabel(String severity) => t('severity_$severity');

  static String _toTitleCase(String snakeCase) => snakeCase
      .split('_')
      .map((w) => w.isEmpty ? '' : w[0].toUpperCase() + w.substring(1))
      .join(' ');
}
